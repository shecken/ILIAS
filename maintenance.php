<script>
function findDownload() {
	var cookies = document.cookie;

	var cookieArray = cookies.split(";");

	for(i = 0; i < cookieArray.length; i++) {
		var cookieContent  = cookieArray[i].split("=");
		if(cookieContent[0].trim() == "download_started" && cookieContent[1].trim() == "started") {
			hideLoader();
		}
	}
}

function initInterval() {
	findDownloadTimer = window.setInterval(findDownload, 600);
}

function hideLoader() {
	document.getElementById("loader_background").style.display = "none";
	document.getElementById("loader_image").style.display = "none";
	clearInterval(findDownloadTimer);
}

function showLoader() {
	document.getElementById("loader_background").style.display = "block";
	document.getElementById("loader_image").style.display = "inline";
}

var findDownloadTimer;
window.setTimeout(hideLoader, 0);
window.addEventListener("beforeunload", showLoader);
window.addEventListener("beforeunload", initInterval);
</script>
